// Ring loss — what makes a ring worth anything.
// ---------------------------------------------------------------------------

/// How many of the rings you were carrying actually scatter. The rest are gone.
///
/// Losing everything and being able to recover everything are both boring; the
/// classic answer is "you can get SOME of it back if you are quick", and the cap
/// is what keeps a big purse from turning a hit into a shower.
const SCATTERED_RINGS_MAX: usize = 12;

/// Outward launch speed (px/s) a scattered ring bursts from the body with.
const SCATTER_BURST_SPEED: f32 = 250.0;

/// Downward pull (px/s²) that arcs a scattered ring back down, screen-relative
/// (the demo runs under normal screen gravity).
const SCATTER_GRAVITY: f32 = 900.0;

/// How long (s) a scattered ring is airborne-and-uncollectible by the magnet:
/// during this window `arc_scattered_rings` OWNS its position (bursting outward),
/// so the coin magnet cannot vacuum it straight back the instant it drops. After
/// it, the ring is an ordinary currency pickup the shared economy collects —
/// that hand-off is what makes "run back and grab them" a real scramble rather
/// than a same-frame refund. Just short of a second, the classic feel.
const SCATTER_LOCK_S: f32 = 0.85;

/// Marks the body's "I just lost my rings" window, so a single hit cannot be
/// billed twice while the damage frame is still resolving.
#[derive(bevy::prelude::Component, Debug, Default)]
pub struct SanicRingLoss {
    /// Health the body had last time this ran. A DROP is the hit edge.
    seen_health: Option<i32>,
}

/// A ring in mid-BURST after a hit (classic Sonic scatter): it flies out from the
/// body and arcs down under gravity, and while its `lock` is live it is NOT yet
/// magnet-collectible. Rollback-registered sim state — the burst has to
/// resimulate identically, so its velocity/position/lock ride the snapshot the
/// same way `BallDash` does. `arc_scattered_rings` integrates it; when the lock
/// expires the component is removed and the ring becomes an ordinary pickup.
#[derive(bevy::prelude::Component, Clone, Copy, Debug)]
pub struct ScatteredRing {
    /// Current burst velocity (screen frame, px/s).
    pub vel: ae::Vec2,
    /// The position this system OWNS while locked — integrated here so it wins
    /// over the coin magnet, which also writes the pickup's `CenteredAabb`.
    pub arc_pos: ae::Vec2,
    /// Seconds left in the airborne/uncollectible window.
    pub lock: f32,
}

/// **Rings absorb the hit.**
///
/// This is the rule the whole ring economy exists to serve: carrying rings is
/// not a score, it is a life. Take a hit holding rings and you survive it and
/// they scatter; take one holding none and it lands normally.
///
/// The hit is detected as a DROP in the body's health rather than by listening
/// for a damage message, for the same reason Mary-O counts deaths off the
/// engine's respawn counter: every current and future source of damage — badnik
/// contact, spikes, a hazard nobody has authored yet — is already accounted for,
/// with no per-source wiring.
///
/// The scattered rings are ordinary `currency` pickups spawned through the
/// engine's own `spawn_pickup`, so the shared economy collects them on the exact
/// path an authored ring takes. A dropped ring and an authored ring are the same
/// thing once they exist, which is what makes "run back and grab them" work
/// without a single line of demo collection code.
pub fn scatter_rings_on_hit(
    mut commands: bevy::prelude::Commands,
    active_session: Option<bevy::prelude::Res<ambition::platformer::lifecycle::ActiveSessionScope>>,
    mut vfx: bevy::prelude::MessageWriter<ambition::vfx::VfxMessage>,
    mut sfx: ambition::sfx::SfxWriter,
    mut bodies: bevy::prelude::Query<
        (
            bevy::prelude::Entity,
            &ae::BodyKinematics,
            &mut ambition::characters::actor::BodyHealth,
            &mut ambition::characters::actor::BodyWallet,
            Option<&mut SanicRingLoss>,
        ),
        ambition::platformer::markers::PrimaryPlayerOnly,
    >,
) {
    let Some(scope) =
        ambition::platformer::lifecycle::SessionSpawnScope::for_optional_active_session(
            active_session.as_deref(),
        )
    else {
        return;
    };

    for (entity, kin, mut health, mut wallet, loss) in &mut bodies {
        let current = health.health.current;
        let Some(mut loss) = loss else {
            // First sight: adopt the health we find rather than reading the
            // session's starting value as a hit.
            commands.entity(entity).try_insert(SanicRingLoss {
                seen_health: Some(current),
            });
            continue;
        };
        let previous = *loss.seen_health.get_or_insert(current);
        let took_a_hit = current < previous;
        loss.seen_health = Some(current);

        if !took_a_hit || wallet.balance <= 0 {
            // No rings: the hit lands exactly as it would have. Ring loss is a
            // privilege of carrying rings, not a blanket shield.
            continue;
        }

        // Carrying rings: the hit is SPENT on them instead of on health.
        health.health.current = previous;
        let carried = wallet.balance;
        wallet.balance = 0;

        let scattered = (carried as usize).min(SCATTERED_RINGS_MAX);
        for i in 0..scattered {
            // Classic Sonic: the rings BURST from the body and arc outward under
            // gravity, rather than appearing in a static fan. Each ring launches
            // up-and-out; alternating x-sign fans the spray symmetrically to both
            // sides. The arc + the no-magnet lock live on `ScatteredRing`, ticked
            // by `arc_scattered_rings`.
            let t = (i as f32 + 0.5) / scattered as f32;
            let angle = std::f32::consts::PI * (0.18 + 0.64 * t);
            let dir = if i % 2 == 0 { 1.0 } else { -1.0 };
            let vel = ae::Vec2::new(
                angle.cos() * SCATTER_BURST_SPEED * dir,
                // Screen +y is down, so a negative y launches them upward.
                -angle.sin() * SCATTER_BURST_SPEED,
            );
            let size = ae::Vec2::splat(18.0);
            let authored = ambition::actors::rooms::Authored {
                id: format!("sanic_dropped_ring_{}_{i}", entity.index()),
                name: "ring".to_string(),
                // Every ring is born AT the body and flies out from there.
                aabb: ae::Aabb::new(kin.pos, size * 0.5),
                payload: {
                    let mut spec = ambition::entity_catalog::placements::PickupSpec::new(
                        ambition::entity_catalog::placements::PickupKindSpec::Currency {
                            amount: 1,
                        },
                    );
                    spec.sprite = Some(RING_SPRITE_KIND.to_string());
                    spec
                },
            };
            let ring = ambition::actors::features::ecs::spawn_static::spawn_pickup(
                &mut commands,
                scope,
                &authored,
            );
            commands.entity(ring).insert(ScatteredRing {
                vel,
                arc_pos: kin.pos,
                lock: SCATTER_LOCK_S,
            });
        }

        vfx.write(ambition::vfx::VfxMessage::Burst {
            pos: kin.pos,
            count: 18,
            speed: 210.0,
            color: [1.0, 0.86, 0.28, 1.0],
            kind: ambition::vfx::ParticleKind::Dust,
        });
        sfx.write(ambition::sfx::SfxMessage::Play {
            id: ambition::sfx::SfxId::from_static(SFX_BADNIK),
            pos: kin.pos,
        });
    }
}

/// Fly the mid-burst scattered rings outward and arc them down (classic Sonic).
///
/// Ordered `.after(magnetize_pickups).before(collect_ecs_pickups)`: it OWNS the
/// ring's `CenteredAabb` during the lock, so the coin magnet — which wrote the
/// same AABB a step earlier — cannot reel the ring straight back, and `collect`
/// then sees the ring out at its arced position (well away from the knocked-back
/// body) instead of on top of the player. When the lock expires the component is
/// dropped and the ring is an ordinary currency pickup again, collectible on the
/// shared path. Deterministic (all state is the rollback-registered
/// `ScatteredRing` plus the scaled sim dt), so it resimulates identically.
pub fn arc_scattered_rings(
    mut commands: bevy::prelude::Commands,
    time: bevy::prelude::Res<ambition::time::WorldTime>,
    mut rings: bevy::prelude::Query<(
        bevy::prelude::Entity,
        &mut ScatteredRing,
        &mut ae::CenteredAabb,
    )>,
) {
    let dt = time.scaled_dt;
    for (entity, mut ring, mut aabb) in &mut rings {
        // The burst arc is authoritative while locked — overwrite whatever the
        // magnet just did to the AABB.
        aabb.center = ring.arc_pos;
        let step = ring.vel * dt;
        ring.arc_pos += step;
        ring.vel.y += SCATTER_GRAVITY * dt;
        ring.lock -= dt;
        if ring.lock <= 0.0 {
            commands.entity(entity).remove::<ScatteredRing>();
        }
    }
}

// ---------------------------------------------------------------------------
// The goal — what an act is FOR.
// ---------------------------------------------------------------------------

/// World x of the goal line.
///
/// Set back from the level's right edge because the body cannot reach the edge
/// itself: the runnable extent tops out around `LEVEL_WIDTH - 270`, so a goal
/// any closer to the wall is one a player can never cross. The first version of
/// this sat at `LEVEL_WIDTH - 130` and made the act literally uncompletable —
/// you could run the speedway forever and nothing would ever happen.
pub const GOAL_X: f32 = LEVEL_WIDTH - 400.0;

/// How long the results card holds before the act restarts.
pub const ACT_CLEAR_DWELL: f32 = 4.0;

/// Points per second of time left on a nominal par run, and the par itself.
/// A time bonus is what makes going FAST pay, which is the entire premise of
/// this demo — without it, a route that is safer but slower always wins.
///
/// Par is 30s against a measured ~6s clean run (see the act-completion proof),
/// so the bonus spreads meaningfully across real finishing times instead of
/// bunching every plausible run near the maximum.
pub const ACT_PAR_SECONDS: f32 = 30.0;
pub const TIME_BONUS_PER_SECOND: i32 = 100;
/// Each ring you finish holding is worth this.
///
/// Sized against the time bonus deliberately: a clean run banks roughly 2400
/// from time, and a full ring purse is worth roughly as much again. Two
/// currencies only pull against each other if they are of comparable size — at
/// the original 10/ring the time bonus swamped rings by an order of magnitude
/// and the "tension" this demo is built on did not exist in the arithmetic.
pub const RING_BONUS: i32 = 100;

/// The act's score, from the numbers captured at the clear.
pub fn act_score(time: f32, rings: i32) -> i32 {
    let remaining = (ACT_PAR_SECONDS - time).max(0.0);
    (remaining * TIME_BONUS_PER_SECOND as f32) as i32 + rings.max(0) * RING_BONUS
}

/// Format a duration the way a results screen does.
pub fn act_time_text(seconds: f32) -> String {
    let whole = seconds.max(0.0) as i32;
    format!("{}:{:02}", whole / 60, whole % 60)
}

/// **Crossing the goal clears the act.**
///
/// Captures the time and the rings AT THE CLEAR — the results card reads those,
/// not the live values, so picking a ring up during the outro cannot rewrite a
/// result the player is already looking at.
pub fn clear_act_at_goal(
    player: bevy::prelude::Query<
        (
            &ae::BodyKinematics,
            &ambition::characters::actor::BodyWallet,
        ),
        ambition::platformer::markers::PrimaryPlayerOnly,
    >,
    mut act: bevy::prelude::Query<&mut SanicActState>,
    mut sfx: ambition::sfx::SfxWriter,
    mut vfx: bevy::prelude::MessageWriter<ambition::vfx::VfxMessage>,
) {
    let Ok((kin, wallet)) = player.single() else {
        return;
    };
    for mut state in &mut act {
        if !matches!(state.phase, SanicActPhase::Running) || kin.pos.x < GOAL_X {
            continue;
        }
        state.phase = SanicActPhase::Cleared {
            time: state.elapsed,
            rings: wallet.balance,
            dwell: ACT_CLEAR_DWELL,
        };
        vfx.write(ambition::vfx::VfxMessage::Burst {
            pos: kin.pos,
            count: 40,
            speed: 320.0,
            color: [1.0, 0.92, 0.35, 1.0],
            kind: ambition::vfx::ParticleKind::Dust,
        });
        sfx.write(ambition::sfx::SfxMessage::Play {
            id: ambition::sfx::SfxId::from_static(SFX_TRANSFORM),
            pos: kin.pos,
        });
    }
}

/// **The act restarts once the results have been read.**
///
/// Same shape as Mary-O's cyclic level: the dwell is what makes the tally
/// legible before the world rebuilds under it, and the restart itself is the
/// engine's ordinary `RoomReplayRequested` rather than anything demo-specific.
pub fn cycle_act_after_clear(
    time: bevy::prelude::Res<ambition::time::WorldTime>,
    mut act: bevy::prelude::Query<&mut SanicActState>,
    mut replay: bevy::prelude::MessageWriter<ambition::actors::session::reset::RoomReplayRequested>,
) {
    for mut state in &mut act {
        let SanicActPhase::Cleared { dwell, .. } = &mut state.phase else {
            continue;
        };
        *dwell -= time.scaled_dt;
        if *dwell > 0.0 {
            continue;
        }
        *state = SanicActState::default();
        replay.write(ambition::actors::session::reset::RoomReplayRequested);
    }
}
