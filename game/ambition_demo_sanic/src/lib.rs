/// Register Sanic's immutable authored definitions in one Bevy `App`: the
/// character fragment and the provider-indexed music/SFX fragments. Shared by the historical
/// [`SanicDemoContentPlugin`] (Startup-driven construction) and the new
/// [`provider::SanicExperiencePlugin`] (shell-activation-driven construction), so
/// there is one definition of Sanic's content seam.
///
/// The registered App-local resources are the sole composition authority.
/// Runtime simulation, presentation, dialogue, and combat consumers receive
/// the assembled catalog explicitly through Bevy resources.
pub fn install_sanic_content(app: &mut App) {
    use ambition::audio::catalog::{AudioCatalogAppExt, AudioCatalogFragment};
    use ambition::characters::actor::character_catalog::{
        CharacterCatalogAppExt, CharacterCatalogFragment,
    };

    app.register_character_catalog_fragment(
        CharacterCatalogFragment::from_ron(
            provider::SANIC_EXPERIENCE,
            Some(SANIC_CHARACTER_ID),
            SANIC_CATALOG_RON,
        )
        .expect("Sanic character catalog should be valid"),
    );
    badnik::register_badnik_roster(app);
    app.register_audio_catalog_fragment(
        AudioCatalogFragment::new(
            provider::SANIC_EXPERIENCE,
            Some(ambition::audio::spec::MusicRegistry {
                default_track: "you_are_too_slow".to_string(),
                tracks: vec![ambition::audio::spec::MusicTrack {
                    id: "you_are_too_slow".to_string(),
                    display_name: "You Are Too Slow".to_string(),
                    asset_path: Some(SANIC_MUSIC_ASSET_PATH.to_string()),
                }],
            }),
            // Sanic AUTHORS the cues it emits (Dash, Jump) rather than
            // inheriting them from the resident Ambition synth table. Declaring
            // them is what authorizes them under provider-relative SFX: a Sanic
            // session plays these and nothing else, and an Ambition-only bank id
            // resident in the shared bank cannot resolve here.
            Some(ambition::audio::spec::SfxRegistry {
                sample_rate: 44_100,
                sfx: sanic_sfx_specs(),
            }),
        )
        .expect("Sanic audio catalogs should be valid"),
    );
    // The animated ring sheet is CONTENT, not a property of one shell: register
    // it here so it loads identically whether Sanic runs standalone or as a mode
    // in the multi-game host (both add `SanicExperiencePlugin`, which calls this).
    // A prop is not carried in the lean sanic asset catalog, so this mirrors
    // Ambition's own intro-prop loader (a per-frame "insert if missing" GameAssets
    // mutation) rather than the catalog-fragment path — see smell #19.
    app.add_systems(bevy::prelude::Update, register_sanic_ring_prop_sheet);

    // Sanic's mutable sim state joins the rollback contract through the same
    // seam engine crates use — here, before either construction path
    // fingerprints the App, so the schema fingerprint (part of the content
    // identity) includes these rows; a non-GGRS shell records metadata only.
    {
        use ambition::runtime::rollback::AmbitionRollbackApp;
        // The act-state ANCHOR comes first: the mode owner is a bare
        // state-holder entity no engine rollback anchor reaches, and a
        // registered-but-unanchored component silently never snapshots (the
        // Mary-O behavioral restore test caught this class: a dirty mutation
        // survived a GGRS rollback). `BallDash`/`BallDashInput` ride the
        // player body, which `entity:body_kinematics` already anchors.
        app.require_rollback::<SanicActState>("ambition_demo_sanic", "entity:sanic_mode_owner")
            .rollback_component_clone::<ball_dash::BallDash>(
                "ambition_demo_sanic",
                "content.sanic_ball_dash",
            )
            .rollback_component_clone::<ball_dash::BallDashInput>(
                "ambition_demo_sanic",
                "content.sanic_ball_dash_input",
            )
            .rollback_component_clone::<SanicActState>(
                "ambition_demo_sanic",
                "content.sanic_act_state",
            )
            // A mid-burst scattered ring rides the currency pickup it decorates,
            // which `CenteredAabb`/`PickupFeature` already anchor + snapshot — so
            // like `BallDash` on the body, it only needs its own component
            // registered. Without this the burst would silently not resimulate.
            .rollback_component_clone::<ScatteredRing>(
                "ambition_demo_sanic",
                "content.sanic_scattered_ring",
            );
    }
}

/// Ensure the animated `sanic_ring_prop` sheet lives in `GameAssets.props` so the
/// pickup renderer binds it (the ring's spinning sprite) instead of the static
/// coin. Insert-if-missing rather than a one-shot latch: it self-heals after the
/// host's wholesale `GameAssets` rebuild (the quality-scale reload wipes props),
/// and the insert flips `GameAssets::is_changed()` so the prop-rebind pass runs.
/// No-ops in a headless/sim-only build where no `GameAssets` (or renderer) exists.
fn register_sanic_ring_prop_sheet(
    game_assets: Option<bevy::prelude::ResMut<ambition::sprite_sheet::game_assets::GameAssets>>,
    config: Option<bevy::prelude::Res<ambition::sprite_sheet::game_assets::GameAssetConfig>>,
    asset_server: Option<bevy::prelude::Res<bevy::prelude::AssetServer>>,
    layouts: Option<
        bevy::prelude::ResMut<bevy::prelude::Assets<bevy::prelude::TextureAtlasLayout>>,
    >,
) {
    let (Some(mut game_assets), Some(config), Some(asset_server), Some(mut layouts)) =
        (game_assets, config, asset_server, layouts)
    else {
        return;
    };
    if config.no_assets || game_assets.characters.props.contains_key(RING_SPRITE_KIND) {
        return;
    }
    if let Some(asset) = ambition::actors::character_sprites::load_prop_sheet_for_target(
        &asset_server,
        &mut layouts,
        &config.sprite_folder,
        RING_SPRITE_KIND,
        &ambition::sprite_sheet::character::SheetTuning::new(1.0, 2),
    ) {
        game_assets
            .characters
            .props
            .insert(RING_SPRITE_KIND.to_string(), asset);
    }
}

