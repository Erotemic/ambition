/// The act's live state, owned by the mode. It rides a `ModeScopedEntity`, so
/// leaving the Sanic rooms tears it down through the engine's lifetime-scope
/// vocabulary rather than a bespoke reset.
#[derive(bevy::prelude::Component, Clone, Copy, Debug, Default, PartialEq)]
pub struct SanicActState {
    /// Seconds the act has been running (sim clock, so bullet-time slows it).
    pub elapsed: f32,
    /// Running vs cleared. The clock stops on a clear, which is what turns the
    /// elapsed time from a stopwatch into a RESULT.
    pub phase: SanicActPhase,
    /// Next index in [`SPEED_MARKER_XS`] that should emit its one-shot progress
    /// cue. Mode-scoped with the act, so leaving and re-entering the demo resets
    /// the audible ruler without a global resource leak.
    pub next_milestone: usize,
}

/// Where the act is. `Cleared` carries the numbers the results card reads,
/// captured at the instant of the clear rather than re-derived afterwards —
/// otherwise the rings you pick up during the outro would keep changing your
/// result while you look at it.
#[derive(Clone, Copy, Debug, Default, PartialEq)]
pub enum SanicActPhase {
    #[default]
    Running,
    Cleared {
        time: f32,
        rings: i32,
        /// Seconds the results card has left before the act restarts.
        dwell: f32,
    },
}

/// Sanic's level rules. **ONE system list; a constructor flag decides its gating**
/// — [`SanicRulesPlugin::hosted`] when Ambition hosts the demo alongside its own
/// rooms, [`SanicRulesPlugin::global`] when the demo IS the game. That is the D-C
/// pattern (`docs/planning/engine/decomposition.md` §Phase D-C), and this is its
/// first real consumer: before this, `in_mode` had no ruleset to gate.
pub struct SanicRulesPlugin {
    hosted: bool,
}

impl SanicRulesPlugin {
    /// Ambition hosts this demo: every rule sleeps outside the Sanic rooms.
    pub fn hosted() -> Self {
        Self { hosted: true }
    }

    /// The demo IS the game: the rules run unconditionally.
    pub fn global() -> Self {
        Self { hosted: false }
    }
}

impl Plugin for SanicRulesPlugin {
    fn build(&self, app: &mut App) {
        // The plugin OWNS its mandatory message channels and shared resources:
        // its systems write SFX cues, pop VFX bursts, read room-load facts, and
        // contribute to the collision overlay. A full app registers all of
        // these through the engine plugins; a thin rules-only harness may not,
        // and `add_message`/`init_resource` are idempotent.
        app.add_message::<ambition::sfx::OwnedSfxMessage>();
        app.add_message::<ambition::vfx::VfxMessage>();
        app.add_message::<ambition::actors::rooms::RoomLoaded>();
        // The act cycle restarts the room on a clear, exactly as Mary-O's level
        // cycle does. The engine registers this in a full app; a thin
        // rules-only harness may not, and `add_message` is idempotent.
        app.add_message::<ambition::actors::session::reset::RoomReplayRequested>();
        app.init_resource::<ambition::actors::features::FeatureEcsWorldOverlay>();
        use bevy::prelude::IntoScheduleConfigs;
        let sim = ambition::platformer::schedule::SimScheduleExt::sim_schedule(app);
        app.init_resource::<ball_dash::BallDashTuning>();
        // BEFORE the persona gate: attach the mode-local state + DECLARE the
        // spin-dash technique on the Attack slot, so the shared resolver inside
        // `gate_worn_player_control` resolves Attack -> `Technique("spin_dash")`
        // and routes its device edge into the sanctioned `ResolvedTechniqueEdges`
        // (clearing the raw melee verb). Utility is D in the classic preset; the
        // transform system consumes that edge so it cannot also toggle a host-code
        // flight ability inherited by the control box.
        let sanic_pre_gate = (ball_dash::attach_ball_dash, toggle_sanic_form)
            .chain()
            .in_set(ambition::platformer::schedule::SandboxSet::PlayerInput)
            .after(ambition::actors::avatar::tick_player_brains)
            .before(ambition::actors::avatar::gate_worn_player_control);
        // AFTER the gate: read Sanic's spin-dash rev from the sanctioned technique
        // edge the gate resolved — the fragile before-gate `melee_pressed`
        // interception window is GONE (a plain melee edge is no longer the API).
        let sanic_post_gate = ball_dash::capture_ball_dash_input
            .in_set(ambition::platformer::schedule::SandboxSet::PlayerInput)
            .after(ambition::actors::avatar::gate_worn_player_control);
        if self.hosted {
            app.add_systems(
                sim,
                sanic_pre_gate.run_if(ambition::runtime::in_mode(SANIC_MODE)),
            );
            app.add_systems(
                sim,
                sanic_post_gate.run_if(ambition::runtime::in_mode(SANIC_MODE)),
            );
        } else {
            app.add_systems(sim, sanic_pre_gate);
            app.add_systems(sim, sanic_post_gate);
        }

        // The ball dash is a RULE, not world content: it exists while the Sanic
        // mode is live and nowhere else, exactly like the act clock. Effects run
        // after PlayerInput captured the technique and before later presentation.
        // `tick_ball_dash` precedes `tick_rolling`, so a launch cannot un-ball in
        // the same frame even if tuning changes.
        let rules = (
            spawn_sanic_mode_owner,
            tick_sanic_act,
            ball_dash::tick_ball_dash,
            ball_dash::tick_rolling,
            // Project the roll authority (`Rolling`) onto the anim fact AFTER
            // both the launch (insert) and the stand-up (remove) have settled,
            // so the looping `ball` row tracks the physical ball for the frame.
            ball_dash::mirror_ball_anim_fact,
            // The super form's derived traits (invincibility + sparkles) track
            // the worn identity every frame — toggle- and monitor-agnostic.
            sync_super_form_traits,
            // Braking scrape on the skid onset (reads the published skid fact).
            emit_sanic_skid_sfx,
            // The goal, then the cycle: a clear captured this frame must be
            // read by the card before the dwell can retire it.
            clear_act_at_goal,
            cycle_act_after_clear,
        )
            .chain()
            .in_set(ambition::platformer::schedule::SandboxSet::GameplayEffects);
        let milestone_sfx = emit_sanic_milestone_sfx
            .in_set(ambition::platformer::schedule::SandboxSet::GameplayEffects);
        // The badnik defeat runs BEFORE the engine's shared body-contact-damage
        // pass so a stomp/roll never also hurts Sanic (the rule zeroes the
        // badnik's health that frame; the contact pass skips a dead attacker).
        let badniks =
            badnik::defeat_badniks.before(ambition::actors::features::apply_actor_contact_damage);
        // Ring loss reads the health the contact pass just wrote, so it runs
        // AFTER it — the hit has to have landed before rings can absorb it.
        let ring_loss =
            scatter_rings_on_hit.after(ambition::actors::features::apply_actor_contact_damage);
        // The scattered-ring burst flies out BETWEEN the coin magnet and the
        // collect: it owns each ring's position during the lock so the magnet
        // can't reclaim it, and collect then sees the ring out at its arc rather
        // than on top of the knocked-back body.
        let scatter_arc = arc_scattered_rings
            .after(ambition::actors::features::magnetize_pickups)
            .before(ambition::actors::features::collect_ecs_pickups);
        // Monitor boxes: re-arm on (re)load, break on stomp/roll, tick the
        // speed-shoes grant. Broken monitors contribute to the overlay's
        // `removed_block_names` AFTER the engine's per-frame rebuild clears it
        // (the same slot Mary-O's bricks and encounter lock walls take).
        app.init_resource::<monitors::SpentMonitors>();
        let monitor_rules = (
            monitors::rearm_monitors_on_room_loaded,
            monitors::break_monitor_boxes,
            monitors::tick_speed_shoes,
        );
        let monitor_overlay = monitors::contribute_broken_monitors_to_overlay
            .after(ambition::actors::features::rebuild_feature_ecs_world_overlay);
        if self.hosted {
            app.add_systems(sim, rules.run_if(ambition::runtime::in_mode(SANIC_MODE)));
            app.add_systems(
                sim,
                milestone_sfx.run_if(ambition::runtime::in_mode(SANIC_MODE)),
            );
            app.add_systems(sim, badniks.run_if(ambition::runtime::in_mode(SANIC_MODE)));
            app.add_systems(
                sim,
                ring_loss.run_if(ambition::runtime::in_mode(SANIC_MODE)),
            );
            app.add_systems(
                sim,
                scatter_arc.run_if(ambition::runtime::in_mode(SANIC_MODE)),
            );
            app.add_systems(
                sim,
                monitor_rules.run_if(ambition::runtime::in_mode(SANIC_MODE)),
            );
            app.add_systems(
                sim,
                monitor_overlay.run_if(ambition::runtime::in_mode(SANIC_MODE)),
            );
        } else {
            app.add_systems(sim, rules);
            app.add_systems(sim, milestone_sfx);
            app.add_systems(sim, badniks);
            app.add_systems(sim, ring_loss);
            app.add_systems(sim, scatter_arc);
            app.add_systems(sim, monitor_rules);
            app.add_systems(sim, monitor_overlay);
        }
    }
}

/// Play the braking scrape on the frame the controlled body STARTS skidding.
///
/// `BodyMotionFacts::skidding` is published every step by the momentum
/// integration (steer-against-fast-travel while riding); a rising edge fires the
/// scrape once per skid onset instead of every tick it holds. One controlled body
/// plays it, so a `Local` edge latch is sufficient.
fn emit_sanic_skid_sfx(
    subject: Option<bevy::prelude::Res<ambition::platformer::markers::ControlledSubject>>,
    mut was_skidding: bevy::prelude::Local<bool>,
    bodies: bevy::prelude::Query<(&ae::BodyMotionFacts, &ae::BodyKinematics)>,
    mut sfx: ambition::sfx::SfxWriter,
) {
    let skid = subject.and_then(|s| s.0).and_then(|e| bodies.get(e).ok());
    let (skidding, pos) = skid.map_or((false, ae::Vec2::ZERO), |(facts, kin)| {
        (facts.skidding, kin.pos)
    });
    if skidding && !*was_skidding {
        sfx.write(ambition::sfx::SfxMessage::Play {
            id: ambition::sfx::SfxId::from_static(SFX_SKID),
            pos,
        });
    }
    *was_skidding = skidding;
}

/// Toggle the controlled body between the two catalog-authored Sanic forms.
///
/// This consumes the already-semantic Utility edge (`D` in the demo's classic
/// keyboard preset), never a raw key. The super row authors the form's boosted
/// movement and `sync_super_form_traits` derives the rest (invincibility,
/// sparkles, badnik destroy-on-touch) from the worn identity every frame — so
/// this toggle grants and revokes the FULL form, and `WornCharacter` stays the
/// single gameplay + presentation authority. No timer: a future ring drain can
/// wear the form off the same way this toggle does.
fn toggle_sanic_form(
    subject: Option<bevy::prelude::Res<ambition::platformer::markers::ControlledSubject>>,
    mut bodies: bevy::prelude::Query<(
        &mut ambition::characters::brain::ActorControl,
        &mut ambition::characters::actor::WornCharacter,
    )>,
) {
    let Some(entity) = subject.and_then(|subject| subject.0) else {
        return;
    };
    let Ok((mut control, mut worn)) = bodies.get_mut(entity) else {
        return;
    };
    if !control.0.fly_toggle_pressed {
        return;
    }

    // Utility belongs to this mode-local transformation. Consume the edge before
    // lower movement layers can interpret it as the generic fly toggle.
    control.0.fly_toggle_pressed = false;
    let next = match worn.id() {
        SANIC_CHARACTER_ID => SUPER_SANIC_CHARACTER_ID,
        SUPER_SANIC_CHARACTER_ID => SANIC_CHARACTER_ID,
        _ => return,
    };
    // The transform SOUND fires from the worn-identity edge in
    // `sync_super_form_traits`, so wearing the form is all this does.
    *worn = ambition::characters::actor::WornCharacter::new(next);
}

/// How often the super form's sparkle trail pulses (sim seconds).
const SUPER_SPARKLE_PERIOD: f32 = 0.14;
/// Radius of the sparkle orbit around the body centre (px).
const SUPER_SPARKLE_RADIUS: f32 = 15.0;
/// Upward bias of the sparkle ring (px, toward the torso) so the falling motes
/// hover AROUND the body rather than pooling at the feet.
const SUPER_SPARKLE_RISE: f32 = 8.0;

/// Derive the super form's non-movement traits from the worn identity, every
/// sim frame. The movement boost is authored on the `super_sanic` catalog row
/// (the wear itself grants it); what the catalog CANNOT express lives here:
///
/// - **Invincibility**: `Health::invulnerable` tracks the worn identity, so
///   badnik contact can't hurt a super Sanic. (Kernel hazards — the pit, the
///   spikes — still reset the body: falling off the world outranks any form.)
/// - **Sparkles**: a golden pulse trails the form, so the transformation reads
///   on screen and not just on the spritesheet.
///
/// The form is NOT a timed grant: the D-toggle (or a future ring drain) wears
/// it off, and every trait here reverts the same frame because it is derived,
/// never stored. Badnik destroy-on-touch derives from the same identity read
/// in `badnik::defeat_badniks`.
fn sync_super_form_traits(
    time: bevy::prelude::Res<ambition::time::WorldTime>,
    mut sparkle_accum: bevy::prelude::Local<f32>,
    mut sparkle_orbit: bevy::prelude::Local<f32>,
    mut was_super: bevy::prelude::Local<bool>,
    mut vfx: bevy::prelude::MessageWriter<ambition::vfx::VfxMessage>,
    mut sfx: ambition::sfx::SfxWriter,
    mut players: bevy::prelude::Query<
        (
            &ambition::characters::actor::WornCharacter,
            &mut ambition::characters::actor::BodyHealth,
            &ae::BodyKinematics,
        ),
        bevy::prelude::With<ambition::actors::actor::PrimaryPlayer>,
    >,
) {
    // `None` = no controlled player (the session has retired; a player is present
    // for every in-session frame, surviving room changes). Derive invincibility
    // and capture the emit position while the body is borrowed.
    let (worn_is_super, pos) = match players.single_mut() {
        Ok((worn, mut health, kinematics)) => {
            let is_super = worn.id() == SUPER_SANIC_CHARACTER_ID;
            if health.health.invulnerable != is_super {
                health.health.invulnerable = is_super;
            }
            (Some(is_super), kinematics.pos)
        }
        Err(_) => (None, ae::Vec2::ZERO),
    };

    // The transform SOUND derives from the worn-identity EDGE, so it fires once no
    // matter what wore the form (the D-toggle, a monitor, a future ring drain). The
    // edge is resolved session-turnover-safe: no controlled player RESETS the latch
    // so the edge state never leaks into the next session — otherwise a session that
    // ended super would fire a phantom detransform on the next session's first
    // frame. These `Local` latches can't be reached by the resource-only
    // `SessionTeardownPlugin`, so this IS their reset-on-retirement.
    let (cue, next_super) = super_form_edge(worn_is_super, *was_super);
    *was_super = next_super;
    if let Some(to_super) = cue {
        sfx.write(ambition::sfx::SfxMessage::Play {
            id: ambition::sfx::SfxId::from_static(if to_super {
                SFX_TRANSFORM
            } else {
                SFX_DETRANSFORM
            }),
            pos,
        });
    }

    // Sparkles only while a super body is present; otherwise the accumulator + orbit
    // reset, so two consecutive super sessions start with identical sparkle phase.
    if worn_is_super != Some(true) {
        *sparkle_accum = 0.0;
        *sparkle_orbit = 0.0;
        return;
    }
    *sparkle_accum += time.scaled_dt;
    if *sparkle_accum >= SUPER_SPARKLE_PERIOD {
        *sparkle_accum -= SUPER_SPARKLE_PERIOD;
        // Orbit the emit point AROUND the body so the motes read as a golden
        // aura ring, not a stream out of one point (Shard's heavy gravity turned
        // a centre-emit burst into a dribble). The golden-angle step spreads
        // successive pulses evenly around the ring; the upward bias keeps the
        // (still-falling) Spark motes hovering around the torso.
        *sparkle_orbit += 2.399_963; // golden angle, radians
        let ring = ae::Vec2::new(sparkle_orbit.cos(), sparkle_orbit.sin()) * SUPER_SPARKLE_RADIUS;
        vfx.write(ambition::vfx::VfxMessage::Burst {
            pos: pos + ring - ae::Vec2::new(0.0, SUPER_SPARKLE_RISE),
            count: 2,
            speed: 26.0,
            color: [1.0, 0.9, 0.4, 1.0],
            kind: ambition::vfx::ParticleKind::Spark,
        });
    }
}

/// The super-form transform-cue edge, resolved so session-local latch state never
/// leaks across a session turnover.
///
/// `worn_is_super` is `None` when no player is controlled (the session has
/// retired). Returns `(cue, next_latch)`: a `Some(true)` cue is a transform, a
/// `Some(false)` a detransform, `None` no cue. With no controlled player the latch
/// RESETS to `false` and no cue fires, so a session that ended super cannot make
/// the next session emit a phantom detransform on its first frame.
fn super_form_edge(worn_is_super: Option<bool>, was_super: bool) -> (Option<bool>, bool) {
    match worn_is_super {
        None => (None, false),
        Some(is_super) if is_super != was_super => (Some(is_super), is_super),
        Some(_) => (None, was_super),
    }
}

/// Bring the act state into being the first frame the mode is live. Spawned
/// `spawn_mode_scoped`, so the engine despawns it when the active room's mode
/// changes — no teardown code here.
fn spawn_sanic_mode_owner(
    mut commands: bevy::prelude::Commands,
    existing: bevy::prelude::Query<(), bevy::prelude::With<SanicActState>>,
    session: Option<bevy::prelude::Res<ambition::platformer::lifecycle::ActiveSessionScope>>,
    mut sfx: ambition::sfx::SfxWriter,
) {
    use ambition::platformer::lifecycle::{SessionSpawnScope, SpawnSessionScopedExt};
    // Sleep when a session-scoped host has retired the live session (i.e. at the
    // launcher): the room metadata may still read "sanic" until the next
    // activation overwrites it, but there is no session to own new state, so the
    // act owner must not be resurrected. When no `ActiveSessionScope` exists at
    // all (the historical Startup path and the D-C tests) the guard is inert.
    let session_live = session
        .as_ref()
        .map_or(true, |scope| scope.current().is_some());
    let spawn_scope = session
        .as_ref()
        .map_or(SessionSpawnScope::UNSCOPED, |scope| scope.spawn_scope());
    if session_live && existing.iter().next().is_none() {
        // Owned by BOTH the mode (survives in-session room changes) and the
        // active session (torn down on a shell relaunch, which a same-mode
        // reload is NOT — so mode-scope alone would leak the act across a
        // launch → quit → relaunch cycle).
        commands
            .spawn_session_scoped(spawn_scope, SanicActState::default())
            .insert(ambition::platformer::lifecycle::ModeScopedEntity(
                SANIC_MODE.to_string(),
            ));
        // Audible confirmation that the standalone shell is draining the
        // standard SfxMessage seam. Distance markers emit alternating cues as
        // the player advances, so this one also proves the bank at room entry.
        sfx.write(ambition::sfx::SfxMessage::Dash {
            pos: ae::Vec2::ZERO,
        });
    }
}

/// The act timer runs on the SIM clock (`scaled_dt`), so bullet-time and pause
/// slow it exactly as they slow everything else — `WorldTime`, never `Res<Time>`.
fn tick_sanic_act(
    time: bevy::prelude::Res<ambition::time::WorldTime>,
    mut act: bevy::prelude::Query<&mut SanicActState>,
) {
    for mut state in &mut act {
        if matches!(state.phase, SanicActPhase::Running) {
            state.elapsed += time.scaled_dt;
        }
    }
}

/// Emit a small, existing Ambition cue when the primary body crosses each
/// visible distance marker. These are deliberately simple diagnostic sounds:
/// the demo is proving that its shell drains the standard [`ambition::sfx::SfxMessage`] seam,
/// not inventing a parallel Sanic audio stack.
fn emit_sanic_milestone_sfx(
    player: bevy::prelude::Query<
        &ae::BodyKinematics,
        bevy::prelude::With<ambition::actors::actor::PrimaryPlayer>,
    >,
    mut act: bevy::prelude::Query<&mut SanicActState>,
    mut sfx: ambition::sfx::SfxWriter,
) {
    let Ok(kin) = player.single() else {
        return;
    };
    for mut state in &mut act {
        while let Some(&marker_x) = SPEED_MARKER_XS.get(state.next_milestone) {
            if kin.pos.x < marker_x {
                break;
            }
            let message = if state.next_milestone % 2 == 0 {
                ambition::sfx::SfxMessage::Dash { pos: kin.pos }
            } else {
                ambition::sfx::SfxMessage::Jump { pos: kin.pos }
            };
            sfx.write(message);
            state.next_milestone += 1;
        }
    }
}

