"""Generate the `symmetry_room` area spec with provable 4-fold (C4) symmetry.

The Noether Chamber. A plus-shaped interior framed by four corner blocks. Each
ARM has its own fixed OUTWARD gravity zone (down/left/up/right) — the four
reference frames — so an Emmy clone standing in each arm is grounded against that
arm's wall and the quartet stays symmetric. At the centre sits the KERNEL, whose
four face switches set the *ambient* (centre) gravity toward that face
(`SetGravityDown|Up|Left|Right` — no colon: the switch payload round-trips
through a ':'-delimited string). Pogo orbs and a 4-colour portal gun (at the
entrance) are the traversal tools. The only asymmetry is the entrance.

Authored by rotating ONE canonical side 90 degrees x3, so symmetry is exact.

    cd tools/ambition_ldtk_tools
    uv run --no-project --python 3.12 python gen_symmetry_room.py
    uv run --python 3.12 python -m ambition_ldtk_tools area create \
        specs/symmetry_room_area.ron --replace-existing
"""

from __future__ import annotations

from pathlib import Path

SIZE = 1280
C = SIZE / 2.0
WALL = 32
ARM = 448
K0, K1 = 512, 768       # kernel extent (256x256, centred)

DIR_ROT = {"down": "left", "left": "up", "up": "right", "right": "down"}
# colon-free action suffix per direction (see encounter/systems.rs handler).
DIR_SUFFIX = {"down": "Down", "up": "Up", "left": "Left", "right": "Right"}


def rot_rect(x, y, w, h):
    """Rotate a rectangle 90 degrees about the room centre (screen y-down)."""
    bx, by = x + w / 2.0, y + h / 2.0
    dx, dy = bx - C, by - C
    nx, ny = (C + (-dy)) - h / 2.0, (C + dx) - w / 2.0
    return int(round(nx)), int(round(ny)), int(round(h)), int(round(w))


def rotn(rect, n):
    for _ in range(n):
        rect = rot_rect(*rect)
    return rect


def fnum(v):
    return str(int(round(v)))


def entity(etype, x, y, w, h, fields):
    parts = ", ".join(f"{k}: {v}" for k, v in fields.items())
    return (f'        ( type: "{etype}", px: [{fnum(x)}, {fnum(y)}], '
            f'size: [{fnum(w)}, {fnum(h)}], fields: ( {parts} ) ),')


# ---- Canonical DOWN-side features; rotated x90 to fill all four arms. ----
CANON_ZONE = (448, 832, 384, 416)        # the down arm's exclusive region
CANON_SWITCH = (608, 772, 64, 28)        # on the kernel's bottom face
CANON_RING = (512, 800, 256, 24)         # shelf just below the kernel (footing)
CANON_POGO = (700, 1150, 36, 36)         # in the arm, to bounce toward the kernel
CANON_EMMY = (560, 1206, 42, 42)         # grounded on the arm's wall by its zone


lines = []
A = lines.append
A('// symmetry_room — "The Noether Chamber": a C4-symmetric gravity tutorial.')
A('//')
A('// GENERATED by tools/ambition_ldtk_tools/gen_symmetry_room.py — do NOT hand-edit;')
A('// edit the generator (it guarantees the quarter-turn symmetry) and re-run, then')
A('//   area create specs/symmetry_room_area.ron --replace-existing')
A('//')
A('// Four arms, each with a fixed OUTWARD gravity zone (the reference frames) and')
A('// one grounded Emmy clone. The central KERNEL has a gravity switch per face that')
A('// sets the ambient (centre) gravity. Pogo orbs + an entrance portal gun are the')
A('// traversal tools. Only the entrance breaks the symmetry.')
A('(')
A('    id: "symmetry_room",')
A('    level_id: "symmetry_room",')
A('    world_x: 120000,')
A('    world_y: 0,')
A(f'    px_wid: {SIZE},')
A(f'    px_hei: {SIZE},')
A('    fill_collision: "empty",')
A('    bg_color: "#12161F",')
A('    biome: "lab",')
A('    music_track: "for_emmy_forever_ago",')
A('    entities: [')

# Border walls (each doubles as a "floor" under the matching gravity).
A('        // Outer border.')
A(entity("Solid", 0, 0, SIZE, WALL, {"name": '"wall_top"'}))
A(entity("Solid", 0, SIZE - WALL, SIZE, WALL, {"name": '"wall_bottom"'}))
A(entity("Solid", 0, 0, WALL, SIZE, {"name": '"wall_left"'}))
A(entity("Solid", SIZE - WALL, 0, WALL, SIZE, {"name": '"wall_right"'}))

# Corner blocks (plus shape).
A('        // Four corner blocks carve the open space into a symmetric plus.')
A(entity("Solid", WALL, WALL, ARM - WALL, ARM - WALL, {"name": '"corner_tl"'}))
A(entity("Solid", SIZE - ARM, WALL, ARM - WALL, ARM - WALL, {"name": '"corner_tr"'}))
A(entity("Solid", WALL, SIZE - ARM, ARM - WALL, ARM - WALL, {"name": '"corner_bl"'}))
A(entity("Solid", SIZE - ARM, SIZE - ARM, ARM - WALL, ARM - WALL, {"name": '"corner_br"'}))

# Kernel.
A('        // The kernel: a gravity switch on each face sets the ambient gravity.')
A(entity("Solid", K0, K0, K1 - K0, K1 - K0, {"name": '"kernel"'}))

# Per-arm features (rotated).
for n in range(4):
    d = ["down", "left", "up", "right"][n]
    A(f'        // ---- {d.upper()} arm: fixed {d}-gravity frame + kernel-{d} switch. ----')
    zx, zy, zw, zh = rotn(CANON_ZONE, n)
    A(entity("GravityZone", zx, zy, zw, zh, {
        "id": f'"grav_{d}"', "name": f'"gravity {d}"', "dir": f'"{d}"'}))
    sx, sy, sw, sh = rotn(CANON_SWITCH, n)
    A(entity("Switch", sx, sy, sw, sh, {
        "id": f'"kernel_switch_{d}"', "name": f'"kernel gravity {d}"',
        "prompt": f'"Set ambient gravity {d.upper()}"',
        "action": f'"SetGravity{DIR_SUFFIX[d]}"', "target_encounter": '""'}))
    rx, ry, rw, rh = rotn(CANON_RING, n)
    A(entity("Solid", rx, ry, rw, rh, {"name": f'"ring_{d}"'}))
    px, py, pw, ph = rotn(CANON_POGO, n)
    A(entity("PogoOrb", px, py, pw, ph, {"name": f'"pogo_{d}"'}))
    ex, ey, ew, eh = rotn(CANON_EMMY, n)
    dialogue = "emmy_noether" if d == "down" else f"emmy_noether_{d}"
    A(entity("NpcSpawn", ex, ey, ew, eh, {
        "prompt": f'"Talk to Emmy No-Ether ({d})"', "dialogue_id": f'"{dialogue}"',
        "character_id": '"npc_noether"', "patrol_radius": "0"}))

# Entrance furniture (the one symmetry-breaker) — all inside the down arm
# open region x[448,832] on the floor, clear of the corner blocks.
A('        // ---- Entrance (the only asymmetry): spawn, door, portal gun. ----')
A(entity("PlayerStart", 470, 1202, 28, 46, {"name": '"symmetry_spawn"'}))
A(entity("PortalGunSpawn", 648, 1208, 40, 40, {
    "id": '"symmetry_portal_gun"', "name": '"Portal Gun (4 colours)"'}))
A(entity("LoadingZone", 752, 1152, 64, 96, {
    "id": '"symmetry_entry"', "name": '"symmetry_entry"', "activation": '"Door"',
    "target_room": '"central_hub_complex"', "target_zone": '"basement_to_symmetry_room_door"',
    "bidirectional": "true"}))

# Signage.
A('        // Signage.')
A(entity("DebugLabel", 472, 560, 352, 24, {
    "name": '"kernel_label"', "text": '"Kernel: a switch on each face sets which way is down."',
    "category": '"Custom"'}))
A(entity("DebugLabel", 460, 1096, 372, 24, {
    "name": '"entry_label"',
    "text": '"Grab the portal gun. Each arm is a different down. Talk to all four Emmys."',
    "category": '"Custom"'}))

A(entity("CameraZone", 0, 0, SIZE, SIZE, {
    "id": '"symmetry_camera"', "name": '"symmetry_camera"', "mode": '"Default"'}))

A('    ],')
A(')')

OUT = Path(__file__).resolve().parent / "specs" / "symmetry_room_area.ron"
OUT.write_text("\n".join(lines) + "\n", encoding="utf8")
print(f"wrote {OUT}")
